class Q3 {
    public static void main(String[] args) {
        int i = 10;
        double d = 5.9;
        float f = 4.5f;

        double result1 = i + d; 
        double result2 = i * f; 
        float result3 = f / i; 

        
        System.out.println("Result of int + double is   : " + result1);
        System.out.println("Result of int * float is    : " + result2);
        System.out.println("Result of float / int is    : " + result3);
    }
}